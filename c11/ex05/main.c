#include "main.h"

int	main(int ac, char **av)
{
	int	a;
	int	b;

	if (ac != 4)
		return (0);
	a = ft_atoi(av[1]);
	b = ft_atoi(av[3]);
	if (av[2][0] != '+' && av[2][0] != '-' && av[2][0] != '*'
	&& av[2][0] != '/' && av[2][0] != '%')
	{
		write(1, "0\n", 2);
		return (0);
	}
	if (av[2][1] != '\0')
	{
		write(1, "0\n", 2);
		return (0);
	}
	to_oper(a, b, av[2][0]);
	return (0);
}
