int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int		idx;

	idx = 0;
	while ((*s1 || *s2) && idx < n)
	{
		idx++;
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	return (0);
}
