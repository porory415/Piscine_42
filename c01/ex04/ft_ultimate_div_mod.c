void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmp1;
	int	tmp2;

	if (*b != 0)
	{
		tmp1 = *a / *b;
		tmp2 = *a % *b;
		*a = tmp1;
		*b = tmp2;
	}
}
